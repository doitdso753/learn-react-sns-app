import { Button } from "@/components/ui/button.tsx";
import type { PostEntity } from "@/types.ts";
import { useOpenAlertModal } from "@/store/alert-modal.ts";
import { useDeletePost } from "@/hooks/mutations/post/use-delete-post.ts";
import { toast } from "sonner";

export default function DeletePostButton(props: PostEntity) {
  const openAlertModal = useOpenAlertModal();
  const { mutate: deletePost, isPending: isDeletePostPending } = useDeletePost({
    onSuccess: () => {
    },
    onError: (error) => {
      toast.error("포스트 삭제에 실패했습니다.", {
        position: "top-center",
      });
    }
  });

  const handleDeleteClick = () => {
    openAlertModal({
      title: "포스트 삭제",
      description: "삭제된 포스트는 되돌릴 수 없습니다. 정말 삭제하시겠습니까?",
      onPositive: () => {
        deletePost(props.id);
      }
    });
  }

  return (
    <Button
      disabled={isDeletePostPending}
      onClick={handleDeleteClick}
      className="cursor-pointer" variant={"ghost"}>
      삭제
    </Button>
  );
}